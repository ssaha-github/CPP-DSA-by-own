#include<iostream>
using namespace std;
int main()
{
    int i=7;
    cout<<++i<<endl;//first i increamented to 8
    cout<<i++<<endl;//now the value of i is 8. so print will be 8 then it's increamented to 9
    cout<<i--<<endl;//print 9 then decreamented to 8
    cout<<--i<<endl;//value 8 is decreamented to 7

}